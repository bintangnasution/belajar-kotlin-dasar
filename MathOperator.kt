fun main() {
    var result1: Int = 10/2
    println(result1)

    val result2 = 10.0/3.0
    println(result2)

    val result3 = 10+10 /2
    println(result3)

//    Augmented assignments
    var total: Int = 0
    val barang1 = 100
    total += barang1
    val barang2 = 200
    total += barang2
    val barang3 = 300
    total += barang3

    println(total)

//    Unary Operator ,++,--,-,+, !
    total--
    println(total)

}